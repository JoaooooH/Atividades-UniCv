#include <stdio.h>

int main(){
    //Tabela de Conversão Fahrenheit para Celsius
    printf("+-----------------------------------------+\n");
    printf("|    Fahrenheit      ->      Celsius      |\n");
    printf("|        1           ->       %.2f      |\n", (5.0/9.0)*(1-32));
    printf("|        2           ->       %.2f      |\n", (5.0/9.0)*(2-32));
    printf("|        3           ->       %.2f      |\n", (5.0/9.0)*(3-32));
    printf("|        4           ->       %.2f      |\n", (5.0/9.0)*(4-32));
    printf("|        5           ->       %.2f      |\n", (5.0/9.0)*(5-32));
    printf("|        6           ->       %.2f      |\n", (5.0/9.0)*(6-32));
    printf("|        7           ->       %.2f      |\n", (5.0/9.0)*(7-32));
    printf("|        8           ->       %.2f      |\n", (5.0/9.0)*(8-32));
    printf("|        9           ->       %.2f      |\n", (5.0/9.0)*(9-32));
    printf("|        10          ->       %.2f      |\n", (5.0/9.0)*(10-32));
    printf("+-----------------------------------------+\n");
    


return 0;
}
